public class Douglas_operator {

   int firstNum = 2;
   int secondNum = 4;

   public Integer sumTwoNums()
   {
      return firstNum+secondNum;
   }

   public Integer multiplyTwoNums()
   {
      return firstNum*secondNum;
   }

   public Integer divideTwoNums()
   {
      return secondNum/firstNum;
   }

   public Integer subtractTwoNums()
   {
      return secondNum-firstNum;
   }
}
